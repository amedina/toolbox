package com.medina.toolbox.trees;

public class BinaryTreeNode {

	public int data;
	BinaryTreeNode parent;
	BinaryTreeNode leftChild;
	BinaryTreeNode rightChild;
	
	public BinaryTreeNode(int data) {
		super();		
		this.data = data;
		parent = null;
		leftChild = null;
		rightChild = null;
	}
	
	
}
